    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // EXECUTE BUTTON
        String sqlCommands = jTextArea1.getText();

        try {
            Statement statement = connection.createStatement();
            result = statement.executeQuery(sqlCommands);
            // rs2xml.jar
            jTable1.setModel(DbUtils.resultSetToTableModel(result));
      
        }catch(SQLException e)  {
            Logger.getLogger(ClientServerMySQL.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null,"Error in SQL Command", "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

