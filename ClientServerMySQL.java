    public void getConnected(){
        // Gets Connection
        String passwordString = new String(jPasswordField1.getPassword());
        String database = String.valueOf(jComboBox2.getSelectedItem());
    
        MysqlDataSource datasource = new MysqlDataSource();
        datasource.setUser(jTextField1.getText());
        datasource.setPassword(passwordString);
        datasource.setUrl(database);    //"jdbc:mysql://127.0.0.1:3312/project3"
    
        try {
            connection = datasource.getConnection();
            jLabel7.setText("Connected to " + database);
        } catch (SQLException ex) {
            Logger.getLogger(ClientServerMySQL.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Could not connect to " + database, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
